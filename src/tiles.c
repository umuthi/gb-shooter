/*
 * 2bpp tile format: each row = 2 bytes (plane-0, plane-1).
 * Color index = (plane1_bit << 1) | plane0_bit.
 *
 * We use only two color indices:
 *   color 0  (plane0=0, plane1=0): background of each tile — maps to BGP shade
 *   color 1  (plane0=1, plane1=0): visible pixels (stars, text, sprites)
 *   color 3  (plane0=1, plane1=1): TILE_BLACK only — solid opaque black
 *
 * BGP_REG = 0xE7:  color0 → shade3(black)  color1 → shade1(lightgray)
 *                  color3 → shade3(black)
 * OBP0_REG = 0xE8: color0 → transparent    color1 → shade2(darkgray)   [player]
 * OBP1_REG = 0xFC: color0 → transparent    color1 → shade3(black)      [bullets/enemies]
 *
 * Result:
 *   Stars      → shade 1 (lightest)   via BGP  color1→shade1
 *   Player     → shade 2              via OBP0 color1→shade2
 *   Bullets    → shade 3 (darkest)    via OBP1 color1→shade3
 *   Enemies    → shade 3 (darkest)    via OBP1 color1→shade3
 */

